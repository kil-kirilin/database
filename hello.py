def demonstrate_data_structures():
    print("--- Демонстрация основных структур данных Python ---")

    # 1. Строки (str) - неизменяемые последовательности символов
    print("\n1. Строки (str):")
    my_string = "Привет, мир!"
    print(f"Исходная строка: '{my_string}'")
    print(f"Длина строки: {len(my_string)}")
    print(f"Первый символ: '{my_string[0]}'")
    print(f"Срез (со 2 по 5 символ): '{my_string[2:6]}'")
    print(f"Строка в верхнем регистре: '{my_string.upper()}'")
    print(f"'мир' присутствует в строке? {'мир' in my_string}")

    # 2. Списки (list) - изменяемые упорядоченные последовательности элементов
    print("\n2. Списки (list):")
    my_list = [1, 2, 3, "четыре", 5.0]
    print(f"Исходный список: {my_list}")
    print(f"Тип элемента по индексу 3: {type(my_list[3])}")
    my_list.append(6)  # Добавление элемента в конец
    print(f"Список после добавления 6: {my_list}")
    my_list.insert(1, "новый") # Вставка элемента по индексу
    print(f"Список после вставки 'новый' по индексу 1: {my_list}")
    my_list.remove("четыре") # Удаление элемента по значению
    print(f"Список после удаления 'четыре': {my_list}")
    my_list[0] = "один" # Изменение элемента по индексу
    print(f"Список после изменения первого элемента на 'один': {my_list}")
    print(f"Элемент 'новый' в списке? {'новый' in my_list}")

    # 3. Кортежи (tuple) - неизменяемые упорядоченные последовательности элементов
    print("\n3. Кортежи (tuple):")
    my_tuple = (10, 20, "тридцать", 40.0)
    print(f"Исходный кортеж: {my_tuple}")
    print(f"Длина кортежа: {len(my_tuple)}")
    print(f"Элемент по индексу 2: {my_tuple[2]}")
    # my_tuple[0] = 50 # Это вызовет ошибку, так как кортежи неизменяемы
    print("Попытка изменить элемент my_tuple[0] = 50 вызовет ошибку (кортежи неизменяемы).")

    # 4. Множества (set) - изменяемые неупорядоченные коллекции уникальных элементов
    print("\n4. Множества (set):")
    my_set = {1, 2, 3, 2, 1, 4} # Дубликаты автоматически удаляются
    print(f"Исходное множество (дубликаты удалены): {my_set}")
    my_set.add(5) # Добавление элемента
    print(f"Множество после добавления 5: {my_set}")
    my_set.remove(2) # Удаление элемента
    print(f"Множество после удаления 2: {my_set}")
    another_set = {3, 4, 6}
    print(f"Другое множество: {another_set}")
    print(f"Объединение множеств: {my_set.union(another_set)}") # Операция объединения
    print(f"Пересечение множеств: {my_set.intersection(another_set)}") # Операция пересечения

    # 5. Словари (dict) - изменяемые неупорядоченные коллекции пар ключ-значение
    print("\n5. Словари (dict):")
    my_dict = {
        "имя": "Алиса",
        "возраст": 30,
        "город": "Москва"
    }
    print(f"Исходный словарь: {my_dict}")
    print(f"Значение по ключу 'имя': {my_dict['имя']}")
    my_dict["профессия"] = "разработчик" # Добавление новой пары
    print(f"Словарь после добавления 'профессия': {my_dict}")
    my_dict["возраст"] = 31 # Изменение значения по ключу
    print(f"Словарь после изменения 'возраст': {my_dict}")
    del my_dict["город"] # Удаление пары ключ-значение
    print(f"Словарь после удаления 'город': {my_dict}")
    print(f"Все ключи словаря: {my_dict.keys()}")
    print(f"Все значения словаря: {my_dict.values()}")
    print(f"'возраст' есть среди ключей? {'возраст' in my_dict}")

# Вызов функции для демонстрации
demonstrate_data_structures()
